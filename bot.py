# ── Утилиты ───────────────────────────────────────────────────────────
def compliment_for(name: str) -> str:
    """Личные комплименты коллеги + общий пул (с подстановкой имени)."""
    personal = PERSONAL_COMPLIMENTS.get(name, [])
    shared = [c.replace("{name}", name) for c in COMPLIMENTS]
    pool = personal + shared
    return random.choice(pool)

def daily_wish_for_today() -> str:
    """Одно и то же пожелание для конкретной даты — детерминированно."""
    today = datetime.now(TZ).date()
    idx = (today.year * 10000 + today.month * 100 + today.day) % len(DAILY_WISHES)
    return DAILY_WISHES[idx]

def names_keyboard() -> InlineKeyboardMarkup:
    buttons = [InlineKeyboardButton(n, callback_data=f"name:{n}") for n in NAMES]
    # по 3 в ряд
    rows = [buttons[i:i + 3] for i in range(0, len(buttons), 3)]
    rows.append([InlineKeyboardButton("🎲 Случайной коллеге", callback_data="name:__random__")])
    return InlineKeyboardMarkup(rows)


# ── Хэндлеры ──────────────────────────────────────────────────────────
async def cmd_start(update: Update, ctx: ContextTypes.DEFAULT_TYPE):
    user = update.effective_user
    text = (
        f"Привет, {user.first_name}! 💗\n\n"
        "Я бот «Тёплые слова» ✨\n"
        "Помогу сказать что-нибудь приятное коллеге.\n\n"
        "Команды:\n"
        "• /wish — выбрать имя и получить комплимент\n"
        "• /daily — пожелание на сегодня\n"
        "• /myid — узнать свой Telegram id (для настройки авторассылки)"
    )
    await update.message.reply_text(text)

async def cmd_myid(update: Update, ctx: ContextTypes.DEFAULT_TYPE):
    uid = update.effective_user.id
    await update.message.reply_text(f"Твой id: `{uid}`", parse_mode="Markdown")

async def cmd_daily(update: Update, ctx: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text("☀️ " + daily_wish_for_today())

async def cmd_wish(update: Update, ctx: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text(
        "Для кого комплимент? 💌",
        reply_markup=names_keyboard(),
    )

async def on_name_pick(update: Update, ctx: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()
    _, name = query.data.split(":", 1)
    if name == "__random__":
        name = random.choice(NAMES)
    text = "🌸 " + compliment_for(name)
    # Показываем результат + предлагаем сгенерировать ещё
    kb = InlineKeyboardMarkup([
        [InlineKeyboardButton("✨ Ещё для " + name, callback_data=f"name:{name}")],
        [InlineKeyboardButton("← К выбору имени", callback_data="back")],
    ])
    await query.edit_message_text(text, reply_markup=kb)

async def on_back(update: Update, ctx: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()
    await query.edit_message_text(
        "Для кого комплимент? 💌",
        reply_markup=names_keyboard(),
    )


# ── Ежедневная рассылка ───────────────────────────────────────────────
async def daily_job(ctx: ContextTypes.DEFAULT_TYPE):
    if not OWNER_ID:
        log.warning("OWNER_ID не задан — пропускаю ежедневную рассылку")
        return
    name = random.choice(NAMES)
    text = (
        "☀️ *Доброе утро!*\n\n"
        f"_{daily_wish_for_today()}_\n\n"
        f"💌 Комплимент дня для коллеги:\n"
        f"🌸 {compliment_for(name)}"
    )
    await ctx.bot.send_message(OWNER_ID, text, parse_mode="Markdown")
    log.info("Ежедневное сообщение отправлено OWNER_ID=%s", OWNER_ID)


# ── Точка входа ───────────────────────────────────────────────────────
def main():
    app = Application.builder().token(BOT_TOKEN).build()

    app.add_handler(CommandHandler("start", cmd_start))
    app.add_handler(CommandHandler("help",  cmd_start))
    app.add_handler(CommandHandler("wish",  cmd_wish))
    app.add_handler(CommandHandler("daily", cmd_daily))
    app.add_handler(CommandHandler("myid",  cmd_myid))
    app.add_handler(CallbackQueryHandler(on_name_pick, pattern=r"^name:"))
    app.add_handler(CallbackQueryHandler(on_back,      pattern=r"^back$"))

    # Запланированная ежедневная задача
    if OWNER_ID:
        app.job_queue.run_daily(
            daily_job,
            time=dtime(hour=DAILY_HOUR, minute=DAILY_MIN, tzinfo=TZ),
            name="daily_wish",
        )
        log.info("Ежедневная рассылка запланирована на %02d:%02d (%s) для %s",
                 DAILY_HOUR, DAILY_MIN, TZ, OWNER_ID)

    log.info("Бот запущен 💗")
    app.run_polling(allowed_updates=Update.ALL_TYPES)


if __name__ == "__main__":
    main()
